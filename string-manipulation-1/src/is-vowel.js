// create a function named isVowel that has one arguement of character
// make a statement that checks to see if all vowels are included as the character
// if they are return true
// if they are not return false
function isVowel(character) {
  if (character === 'a' || character === 'e' || character === 'i' || character === 'o' || character === 'u' || character === 'A' || character === 'E' || character === 'I') {
    return true;
  } else {
    return false;
  }
}
